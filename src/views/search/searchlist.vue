<template>
  <div class="search">
    <van-nav-bar fixed title="商品列表" left-arrow @click-left="$router.go(-1)" />

    <van-search
      readonly
      shape="round"
      background="#ffffff"
      :value="querygoods"
      show-action
      @click="$router.push('/search')"
    >
      <template #action>
        <van-icon class="tool" name="apps-o" />
      </template>
    </van-search>

    <!-- 排序选项按钮 -->
    <div class="sort-btns">
      <div class="sort-item">综合</div>
      <div class="sort-item">销量</div>
      <div class="sort-item">价格 </div>
    </div>

    <div class="goods-list">
      <GoodsItem v-for="item in shuju" :key="item.goods_id" :item="item"></GoodsItem>
    </div>
  </div>
</template>

<script>
import GoodsItem from '@/components/GoodsItem.vue'
import { getgoodlist } from "@/api/searchgoods";
// import Vue from 'vue'
// Vue.component('GoodsItem', GoodsItem)
export default {
  name: 'SearchIndex',
  data(){
    return {
      shuju:'',
      page:1
    }
  },
  components: {
    GoodsItem
  },
  computed:{
    querygoods(){
      return this.$route.query.search
    }
  },
  async created(){
    const res=await getgoodlist({
      categoryId: this.$route.query.categoryId,
      goodsName: this.querygoods,
      page: this.page,
    })
    console.log(res.data.data.list.data);
    this.shuju=res.data.data.list.data
  }
}
</script>

